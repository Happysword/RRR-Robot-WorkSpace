% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
 
l1 = str2double(get(handles.edit1,'String'));
l2 = str2double(get(handles.edit2,'String'));
l3 = str2double(get(handles.edit9,'String'));

q1_min = str2double(get(handles.edit6,'String'));
q1_max = str2double(get(handles.edit5,'String'));
q2_min = str2double(get(handles.edit7,'String'));
q2_max = str2double(get(handles.edit4,'String'));
q3_min = str2double(get(handles.edit8,'String'));
q3_max = str2double(get(handles.edit3,'String'));

%Transform qs into radians
t1=linspace(q1_min, q1_max, 200)*pi/180;
t2=linspace(q2_min, q2_max, 200)*pi/180;
t3=linspace(q3_min, q3_max, 200)*pi/180;

[q1,q2,q3]=ndgrid(t1,t2,t3);  %Creates the matrices of 200x200x200 for each variable 
xM = l1 * cos(q1) + l2 * cos(q1 + q2) + l3 * cos(q1 + q2 + q3);
yM = l1 * sin(q1) + l2 * sin(q1 + q2) + l3 * sin(q1 + q2 + q3);
plot(xM(:),yM(:),'k.')

xL = xlim; %all values of x 
yL = ylim; % all values of y 
line(xL, [0 0],'color','b','linewidth',1) %Draw x-axis
line([0 0], yL,'color','b','linewidth',1) %Draw y-axis


